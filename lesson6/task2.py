# Знайти факторіал рекурсивно

# Рекурсивна функція -> це функція яка визначена в термінах самої себе і здатна викликати саму себе

# 5! -> 1 * 2 * 3 * 4 * 5
# 5! -> 5 * 4! -> 5 * 4 * 3! -> 5 * 4 * 3 * 2! -> 5 * 4 * 3 * 2 * 1! -> 5 * 4 * 3 * 2 * 1

def factorial(n: int):
    if n <= 1:
        return 1
    else:
        return n * factorial(n - 1)  # 5 * 4! -> 5 * 4 * 3!


if __name__ == "__main__":
    print(factorial(5))
