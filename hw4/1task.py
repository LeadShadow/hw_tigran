# написати функцію mul, яка приймає два параметри a, b, порівняти ці параметри, і якщо a > b, тоді
# повернути результат a * 10, якщо рівні, то повернути строку 'параметри рівні', якщо ж параметр b > a
# тоді повернути результат b * 20


def mul(a, b):
    if a > b:
        return a * 10
    elif a == b:
        return 'параметри рівні'
    else:
        return b * 20


